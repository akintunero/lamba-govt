#!/bin/sh
set -e

echo "[init-secrets] Generating dynamic secrets..."

rand_hex() {
  head -c "$1" /dev/urandom | od -An -tx1 | tr -d ' \n'
}

rand_alnum() {
  head -c "$1" /dev/urandom | base64 | tr -dc 'A-Za-z0-9' | head -c "$1"
}

# Generate all secrets
JWT_SECRET="lamba-$(date +%s)-$(rand_hex 32)"
SERVICE_AUTH_TOKEN="lamba-$(date +%s)-$(rand_hex 32)"
MINIO_ACCESS_KEY="minio-$(rand_hex 16)"
MINIO_SECRET_KEY="$(rand_hex 48)"
KEYCLOAK_ADMIN_PASSWORD="kc-$(rand_hex 24)"
KEYCLOAK_CLIENT_SECRET="kc-client-$(rand_hex 32)"
API_GATEWAY_CLIENT_SECRET="gw-$(rand_hex 32)"
SEED_STUDENT_PASSWORD="$(rand_alnum 12)"
SEED_ADMIN_PASSWORD="$(rand_alnum 12)"
DIAGNOSTICS_TOKEN="diag-$(rand_hex 32)"

gen_flag() { echo "FLAG{$(rand_hex 32)}"; }

CTF_FLAG_A01_IDOR=$(gen_flag)
CTF_FLAG_A02_JWT_FORGE=$(gen_flag)
CTF_FLAG_A03_SQLI=$(gen_flag)
CTF_FLAG_A04_PREDICTABLE_RESET=$(gen_flag)
CTF_FLAG_A05_INTERNAL_GATEWAY=$(gen_flag)
CTF_FLAG_A05_METRICS_LINE="lamba_ctf_flag{status=\"$(rand_hex 16)\"}"
CTF_FLAG_A06_PROTOTYPE_POLLUTION=$(gen_flag)
CTF_FLAG_A07_SESSION_FIXATION=$(gen_flag)
CTF_FLAG_A08_MASS_ASSIGNMENT=$(gen_flag)
CTF_FLAG_A09_AUDIT_SPOOF=$(gen_flag)
CTF_FLAG_A10_SSRF=$(gen_flag)
CTF_FLAG_A10_DIAGNOSTICS=$(gen_flag)
CTF_FLAG_CRYPTO_PADDING_ORACLE=$(gen_flag)
CTF_FLAG_FORENSICS_LOG_ANALYSIS=$(gen_flag)
CTF_FLAG_TIMING_ATTACK=$(gen_flag)
CTF_CRYPTO_KEY="$(rand_hex 32)"
CTF_CRYPTO_IV="$(rand_hex 16)"

mkdir -p /secrets
cat > /secrets/.env << EOF
# Auto-generated by init-secrets
JWT_SECRET=${JWT_SECRET}
SERVICE_AUTH_TOKEN=${SERVICE_AUTH_TOKEN}
MINIO_ACCESS_KEY=${MINIO_ACCESS_KEY}
MINIO_SECRET_KEY=${MINIO_SECRET_KEY}
KEYCLOAK_ADMIN_PASSWORD=${KEYCLOAK_ADMIN_PASSWORD}
KEYCLOAK_CLIENT_SECRET=${KEYCLOAK_CLIENT_SECRET}
API_GATEWAY_CLIENT_SECRET=${API_GATEWAY_CLIENT_SECRET}
SEED_STUDENT_PASSWORD=${SEED_STUDENT_PASSWORD}
SEED_ADMIN_PASSWORD=${SEED_ADMIN_PASSWORD}
DIAGNOSTICS_TOKEN=${DIAGNOSTICS_TOKEN}
CTF_FLAG_A01_IDOR=${CTF_FLAG_A01_IDOR}
CTF_FLAG_A02_JWT_FORGE=${CTF_FLAG_A02_JWT_FORGE}
CTF_FLAG_A03_SQLI=${CTF_FLAG_A03_SQLI}
CTF_FLAG_A04_PREDICTABLE_RESET=${CTF_FLAG_A04_PREDICTABLE_RESET}
CTF_FLAG_A05_INTERNAL_GATEWAY=${CTF_FLAG_A05_INTERNAL_GATEWAY}
CTF_FLAG_A05_METRICS_LINE=${CTF_FLAG_A05_METRICS_LINE}
CTF_FLAG_A06_PROTOTYPE_POLLUTION=${CTF_FLAG_A06_PROTOTYPE_POLLUTION}
CTF_FLAG_A07_SESSION_FIXATION=${CTF_FLAG_A07_SESSION_FIXATION}
CTF_FLAG_A08_MASS_ASSIGNMENT=${CTF_FLAG_A08_MASS_ASSIGNMENT}
CTF_FLAG_A09_AUDIT_SPOOF=${CTF_FLAG_A09_AUDIT_SPOOF}
CTF_FLAG_A10_SSRF=${CTF_FLAG_A10_SSRF}
CTF_FLAG_A10_DIAGNOSTICS=${CTF_FLAG_A10_DIAGNOSTICS}
CTF_FLAG_CRYPTO_PADDING_ORACLE=${CTF_FLAG_CRYPTO_PADDING_ORACLE}
CTF_FLAG_FORENSICS_LOG_ANALYSIS=${CTF_FLAG_FORENSICS_LOG_ANALYSIS}
CTF_FLAG_TIMING_ATTACK=${CTF_FLAG_TIMING_ATTACK}
CTF_CRYPTO_KEY=${CTF_CRYPTO_KEY}
CTF_CRYPTO_IV=${CTF_CRYPTO_IV}
EOF

chmod 644 /secrets/.env

echo "[init-secrets] Secrets written to /secrets/.env"
echo "  Student:  student@gov.lamba / ${SEED_STUDENT_PASSWORD}"
echo "  Admin:    admin@gov.lamba / ${SEED_ADMIN_PASSWORD}"
echo "  Keycloak: admin / ${KEYCLOAK_ADMIN_PASSWORD}"
echo "  15 flags generated"
